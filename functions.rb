class Functions
  def ABS(value); value.abs; end
  def ATN(value); Math.atan value; end
  def COS(value); Math.cos value; end
  def EXP(value); Math.exp value; end
  def INT(value); value.to_i; end
  def LOG(value); Math.log value; end
  def SIN(value); Math.sin value; end
  def SQR(value); Math.sqrt value; end
  def TAN(value); Math.tan value; end
  def RND(value); rand value; end
end

